function ft2=wavletDescriptor(im)
grayscale=rgb2gray(im);
[LL,LH,HL,HH]=dwt2(grayscale,'db2');
[LL1,LH1,HL1,HH1]=dwt2(LL,'db2');
[LL2,LH2,HL2,HH2]=dwt2(LL1,'db2');
LL=LL(:)';
LH=LH(:)';
HL=HL(:)';
HH=HH(:)';
ltotal(1) = mean(LL);
ltotal(2) = mean(LH);
ltotal(3) = mean(HL);
ltotal(4) = mean(HH);
sdl(1) = std(LL);
sdl(2) = std(LH);
sdl(3) = std(HL);
sdl(4) = std(HH);
LL1=LL1(:)';
LH1=LH1(:)';
HL1=HL1(:)';
HH1=HH1(:)';
ltotal(5) = mean(LL1);
ltotal(6) = mean(LH1);
ltotal(7) = mean(HL1);
ltotal(8) = mean(HH1);
sdl(5) = std(LL1);
sdl(6) = std(LH1);
sdl(7) = std(HL1);
sdl(8) = std(HH1);
LL2=LL2(:)';
LH2=LH2(:)';
HL2=HL2(:)';
HH2=HH2(:)';
ltotal(9) = mean(LL2);
ltotal(10) = mean(LH2);
ltotal(11) = mean(HL2);
ltotal(12) = mean(HH2);
sdl(9) = std(LL2);
sdl(10) = std(LH2);
sdl(11) = std(HL2);
sdl(12) = std(HH2);
ft2=[];
ft2=[ft2,ltotal,sdl]';
