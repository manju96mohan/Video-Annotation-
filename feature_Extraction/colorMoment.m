function fet=colorMoment(im)
hsv=rgb2hsv(im);
[r,c,~]=size(hsv);
hsv1=hsv(1:r/2,1:c/2,:);
hsv2=hsv(1:r/2,(c/2)+1:c,:);
hsv3=hsv(r/2:r,1:(c/2)+1,:);
hsv4=hsv(r/2:r,c/2:c,:);

sk=[skewness(reshape(hsv(:,:,1),1,numel(hsv(:,:,1)))),skewness(reshape(hsv(:,:,2),1,numel(hsv(:,:,2)))),skewness(reshape(hsv(:,:,3),1,numel(hsv(:,:,3)))),...
    skewness(reshape(hsv1(:,:,1),1,numel(hsv1(:,:,1)))),skewness(reshape(hsv1(:,:,2),1,numel(hsv1(:,:,2)))),skewness(reshape(hsv1(:,:,3),1,numel(hsv1(:,:,3)))),...
    skewness(reshape(hsv2(:,:,1),1,numel(hsv2(:,:,1)))),skewness(reshape(hsv2(:,:,2),1,numel(hsv2(:,:,2)))),skewness(reshape(hsv2(:,:,3),1,numel(hsv2(:,:,3)))),...
    skewness(reshape(hsv3(:,:,1),1,numel(hsv3(:,:,1)))),skewness(reshape(hsv3(:,:,2),1,numel(hsv3(:,:,2)))),skewness(reshape(hsv3(:,:,3),1,numel(hsv3(:,:,3)))),...
    skewness(reshape(hsv4(:,:,1),1,numel(hsv4(:,:,1)))),skewness(reshape(hsv4(:,:,2),1,numel(hsv4(:,:,2)))),skewness(reshape(hsv4(:,:,3),1,numel(hsv4(:,:,3))))];
    
mu =[mean2(hsv(:,:,1)),mean2(hsv(:,:,2)),mean2(hsv(:,:,3)),...
    mean2(hsv1(:,:,1)),mean2(hsv1(:,:,2)),mean2(hsv1(:,:,3)),...
    mean2(hsv2(:,:,1)),mean2(hsv2(:,:,2)),mean2(hsv2(:,:,3)),...
    mean2(hsv3(:,:,1)),mean2(hsv3(:,:,2)),mean2(hsv3(:,:,3)),...
    mean2(hsv4(:,:,1)),mean2(hsv4(:,:,1)),mean2(hsv4(:,:,3))];

sd =[std2(hsv(:,:,1)),std2(hsv(:,:,2)),std2(hsv(:,:,3)),...
    std2(hsv1(:,:,1)),std2(hsv1(:,:,2)),std2(hsv1(:,:,3)),...
    std2(hsv2(:,:,1)),std2(hsv2(:,:,2)),std2(hsv2(:,:,3)),...
    std2(hsv3(:,:,1)),std2(hsv3(:,:,2)),std2(hsv3(:,:,3)),...
    std2(hsv4(:,:,1)),std2(hsv4(:,:,1)),std2(hsv4(:,:,3))];
fet=[sk,mu,sd]';